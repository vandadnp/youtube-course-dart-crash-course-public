void main(List<String> args) {
  // print('Hello' + 2);
  var myAge = 20;
  print(myAge = 30); // myAge = 30
  print(myAge ~/= 2); // 15
  print(myAge *= 2); // 30
  print(myAge += 4); // 34
  print(myAge &= 2); // 2
  print(myAge |= 4); // 6
  print(myAge ^= 10); // 12
  print(myAge -= 10); // 2
}
